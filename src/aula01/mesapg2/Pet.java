package aula01.mesapg2;

public class Pet {
    String type;
    String name;
    int age;
    float foodQuantity;
    String sound;

    public Pet(String type, String name, int age, float foodQuantity, String sound) {
        this.type = type;
        this.name = name;
        this.age = age;
        this.foodQuantity = foodQuantity;
        this.sound = sound;
    }
}
