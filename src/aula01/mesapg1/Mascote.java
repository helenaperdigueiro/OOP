package aula01.mesapg1;

public class Mascote {
    String tipo;
    String nome;
    int idade;
    float qtdComida;
    String som;

    public Mascote(String tipo, String nome, int idade, float qtdComida, String som) {
        this.tipo = tipo;
        this.nome = nome;
        this.idade = idade;
        this.qtdComida = qtdComida;
        this.som = som;
    }
}
